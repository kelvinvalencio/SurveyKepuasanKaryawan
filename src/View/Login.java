    private void btnTransaksiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTransaksiActionPerformed
       if(controller.getLoginState()){
        setView(transaksi);
        
        if(controller.checkIfTheUserHasFilledTheSurvey()){
            //Memanggil fungsi untuk ambil data survey user saat ini
            boolean firstResult = controller.fillCurrentUserSurvey(sDotSelfTextField, ssDotSelfTextField, tsDotSelfTextField, stsDotSelfTextField, nDotSelfTextField);
            if(!firstResult)
                JOptionPane.showMessageDialog(null, "Ada error menerima data survey user..");
       
            //Memanggil fungsi untuk ambil data survey semua user
            boolean secondResult = controller.fillUsersSurvey(sDotOverviewTextField, ssDotOverviewTextField, tsDotOverviewTextField, stsDotOverviewTextField, nDotOverviewTextField);
            if(!secondResult)
                JOptionPane.showMessageDialog(null, "Ada error menerima data survey semua user..");
        }
        else if(!controller.checkIfTheUserHasFilledTheSurvey()){
            //Memanggil fungsi untuk ambil data survey semua user
            boolean secondResult = controller.fillUsersSurvey(sDotOverviewTextField, ssDotOverviewTextField, tsDotOverviewTextField, stsDotOverviewTextField, nDotOverviewTextField);
            if(!secondResult)
                JOptionPane.showMessageDialog(null, "Ada error menerima data survey semua user..");
        }
        
       }
       else{
           JOptionPane.showMessageDialog(null, "Anda belum login, tidak punya akses ke halaman ini.");
       }
    }//GEN-LAST:event_btnTransaksiActionPerformed

