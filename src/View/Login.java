    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // Validasi pada view
        if(id.getText().isEmpty() || pass.getText().isEmpty() || nama.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Masukin datanya dong. Jangan ada yang kosong yah!!");
        }
        else if(id.getText().length() != 8 || pass.getText().length() < 4){
            JOptionPane.showMessageDialog(null, "Password minimal 4 karakter. ID KUDU, MUSTI, WAJIB, HARUS 8 karakter.");
        }
        // Cek ketersediaan ID
        else{
            if(controller.checkIdAvailibility(id.getText())){
                JOptionPane.showMessageDialog(null, "ID sudah digunakan. Mungkin anda sudah punya akun? Hahaha");
            }
            else{
                boolean result = controller.register(id, pass, nama, jk, dv);
                
                if(result){
                    setView(signin);                
                    JOptionPane.showMessageDialog(null, "Register berhasil. Silahkan login.");
                }
                else{
                    JOptionPane.showMessageDialog(null, "Ada kesalahan saat register. Silahkan coba lagi.");
                }
            }
        }
    }//GEN-LAST:event_simpanActionPerformed

