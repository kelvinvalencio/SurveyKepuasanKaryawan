    private void deleteAccountButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteAccountButtonActionPerformed
        int yesButton = JOptionPane.YES_OPTION;
        int resultButton = JOptionPane.showConfirmDialog(null, "Apakah anda yakin untuk hapus akun?", "Anda akan menghapus akun", JOptionPane.YES_NO_OPTION);
        if(resultButton == yesButton){
            JPanel panel = new JPanel();
            JLabel label = new JLabel("Masukan kembali password: ");
            JPasswordField password = new JPasswordField(25);
            panel.add(label);
            panel.add(password);
            String[] options = new String[]{"OK", "Batalkan"};
            int option = JOptionPane.showOptionDialog(null, panel, "Konfirmasi password anda!!", JOptionPane.NO_OPTION,
                                                    JOptionPane.PLAIN_MESSAGE, null, options, options[1]);
            if(option == 0){
                boolean result = controller.deleteAccount(password.getText());
                if(result){
                    JOptionPane.showMessageDialog(null, "Berhasil menghapus akun dan survey anda.");
                    setView(home);
                }
                else{
                    JOptionPane.showMessageDialog(null, "Password salah. Akun gagal dihapus.");
                }
            }
        }
    }//GEN-LAST:event_deleteAccountButtonActionPerformed

